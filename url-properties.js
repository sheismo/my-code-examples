const url = new URL('https://markodenic.com/blog?s=html#section-one');
url.hash //'#section-one'
url.host //'markodenic.com'
url.hostname //'markodenic.com'
url.href //'https://markodenic.com/blog?s=html#section-one'
url.origin //https://markodenic.com
url.pathname //'/blog'
url.protocol //'https:'
url.search //'?s=html'